import turtle


def _goto_dist(x, y, x1, y1, towards=0, size=1):
    turtle.pu()
    turtle.goto(x, y)
    turtle.seth(turtle.towards(x1, y1))
    turtle.fd(turtle.distance(x1, y1) * size)
    turtle.seth(towards)
    turtle.pd()
    return None


